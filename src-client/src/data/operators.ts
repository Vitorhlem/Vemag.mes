export function getOperatorName(badge: string): string {
  if (!badge) return '';
  // Limpa espaços e zeros a esquerda para buscar
  const cleanBadge = String(badge).trim();
  
  if (OPERATORS_MAP[cleanBadge]) {
    return OPERATORS_MAP[cleanBadge];
  }
  
  // Tenta sem zeros a esquerda (Ex: 00003 -> 3) se o map tiver chaves sem zero
  // Mas seu map tem chaves COM zero (ex: '00003'), então a busca exata acima deve funcionar
  
  return '';
}